    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $general = General::find($id);
        $general->email = $request->email;
        $general->phone_number = $request->phone_number;
        $general->first_bank = $request->first_bank;
        $general->second_bank = $request->second_bank;
        $general->facebook = $request->facebook;
        $general->twitter = $request->twitter;
        $general->instagram = $request->instagram;

        $general->save();

        return redirect()->route('dashboard.landing');
    }

